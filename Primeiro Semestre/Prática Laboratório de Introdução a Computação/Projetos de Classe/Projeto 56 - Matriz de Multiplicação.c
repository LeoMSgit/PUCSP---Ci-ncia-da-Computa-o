//Matriz Bidimensional 20 por 20
//Cada elemento será a multiplicação entre o valor da linha e coluna

#include <stdio.h>

int QTLINHA = 20;
int QTCOLUNA = 20;

int main ()
	{int Mat[QTLINHA][QTCOLUNA];
	int i, j;
	int n = 1;
	
	for (i=0; i < QTLINHA; i++)
		for (j=0; j < QTCOLUNA; j++)
			{Mat[i][j] = n*10;
			n++;
			}
	
	for (i=0; i < QTLINHA; i++)
		{for (j=0; j < QTCOLUNA; j++)
			printf("%5d", Mat[i][j]);
		printf("\n");
		}
	}
